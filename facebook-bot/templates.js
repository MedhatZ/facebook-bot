/**
 * @param {{ id: string }} category
 * @param {Date} [date=new Date()]
 * @returns {string}
 */
export function generateFromTemplate(category, date = new Date()) {
  const whatsapp = process.env.WHATSAPP_NUMBER || '01226626676';
  const variants = TEMPLATES[category.id] || TEMPLATES['room-transform'];
  const startOfYear = new Date(date.getFullYear(), 0, 0);
  const dayOfYear = Math.floor((date - startOfYear) / (1000 * 60 * 60 * 24));
  const index = dayOfYear % variants.length;

  return variants[index].replace(/\{whatsapp\}/g, whatsapp);
}

export function shouldUseTemplateOnly() {
  if (process.env.FORCE_TEMPLATE_POSTS === 'true') {
    return true;
  }
  // AgentRouter WAF blocks GitHub Actions datacenter IPs
  if (process.env.GITHUB_ACTIONS === 'true') {
    return true;
  }
  return false;
}
